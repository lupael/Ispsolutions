@push('scripts')
<script src="https://cdn.jsdelivr.net/npm/sortablejs@1.15.0/Sortable.min.js"></script>
<script>
    // Initialize drag and drop
    if (document.getElementById('sortableFields')) {
        new Sortable(document.getElementById('sortableFields'), {
            animation: 150,
            handle: 'tr',
            onEnd: function(evt) {
                // Get new order
                let order = [];
                document.querySelectorAll('#sortableFields tr[data-id]').forEach(function(row) {
                    order.push(row.getAttribute('data-id'));
                });
                
                // Send AJAX request to update order
                fetch('{{ route("panel.admin.custom-fields.reorder") }}', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': '{{ csrf_token() }}'
                    },
                    body: JSON.stringify({ order: order })
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        // Optional: Show success message
                        console.log('Order updated successfully');
                    }
                })
                .catch(error => console.error('Error:', error));
            }
        });
    }
</script>
