<script>
    // Auto-refresh every 30 seconds
    let autoRefresh = false;
    let refreshInterval;

    function toggleAutoRefresh() {
        autoRefresh = !autoRefresh;
        if (autoRefresh) {
            refreshInterval = setInterval(() => {
                window.location.reload();
            }, 30000);
        } else {
            clearInterval(refreshInterval);
        }
    }
</script>
